function getPoolForRule(rule) {
  const catMap = {
    'S15':'animal','S16':'color','S17':'sport','S18':'vehicle','S19':'food',
    'S20':'instrument','S21':'weather','S22':'emotion','S23':'body','S29':'country',
    'S30':'planet','S32':'number','S35':'tree','S36':'gemstone','S37':'mythical',
    'S38':'occupation','S39':'genre','S40':'currency','S41':'popstar','S42':'rockband',
    'S43':'usstate','S44':'worldcapital','S45':'river','S46':'president',
    'S47':'element','S48':'constellation','S49':'actor','S50':'athlete',
    'S51':'landmark','S52':'fictional',
  };
  if (catMap[rule.id]) return CATEGORY_POOL[catMap[rule.id]] || [];
  if (rule.id === 'S05') return [...SILENT_SET];
  if (rule.id === 'S14') return [...PALINDROME_LIST];
  if (rule.id === 'S56') return [...COMPOUND_SET];
  if (rule.id === 'S57') return [...SYLLABLE_SET];

  // Computational rules — single-word entries only
  const allWords = [...new Set([
    ...GENERAL_WORDS,
    ...Object.entries(CATEGORY_POOL)
      .filter(([cat]) => !MULTIWORD_CATS.has(cat))
      .flatMap(([,words]) => words),
    ...[...SILENT_SET],
  ])].filter(w => !isMultiWord(w));

  return VOWEL_RULE_IDS.has(rule.id)
    ? allWords.filter(w => !w.includes('Y'))
    : allWords;
}

function noYFilter(rule, words) {
  return VOWEL_RULE_IDS.has(rule.id) ? words.filter(w => !w.includes('Y')) : words;
}

// Single-word only pool for computational rules and pair phases
function getSingleWordPool() {
  return [...new Set([
    ...GENERAL_WORDS,
    ...Object.entries(CATEGORY_POOL)
      .filter(([cat]) => !MULTIWORD_CATS.has(cat))
      .flatMap(([,words]) => words),
    ...[...SILENT_SET],
  ])].filter(w => !isMultiWord(w));
}

function buildSinglePhase(rule, count, rng, usedWords) {
  const pool     = getPoolForRule(rule);
  const truePool = pool.filter(w => rule.check(w) && !usedWords.has(w));

  // False pool: broad single-word pool for computational rules
  // For category rules: all words not matching the rule
  const broadSingle = noYFilter(rule, getSingleWordPool())
    .filter(w => !rule.check(w) && !usedWords.has(w));

  // For category rules pull from all words including other category entries
  const falsePool = CAT_RULE_IDS.has(rule.id)
    ? [...new Set([
        ...pool.filter(w => !rule.check(w) && !usedWords.has(w)),
        ...broadSingle,
      ])]
    : broadSingle;

  const half       = Math.floor(count / 2);
  const trueCount  = half;
  const falseCount = count - half;

  // ── Distractor logic: fill up to 50% of false slots from curated distractors ──
  const distractorSlots = Math.floor(falseCount * 0.5);
  const rawDistractors  = (DISTRACTOR_POOL[rule.id] || [])
    .filter(w => !rule.check(w) && !usedWords.has(w));
  const distractorWords = pickN(rawDistractors, distractorSlots, rng);
  distractorWords.forEach(w => usedWords.add(w));

  // Backfill remaining false slots from the broad pool (excluding used)
  const backfillPool  = falsePool.filter(w => !usedWords.has(w));
  const backfillCount = falseCount - distractorWords.length;
  const backfillWords = pickN(backfillPool, backfillCount, rng);

  if (truePool.filter(w => !usedWords.has(w)).length < trueCount) return null;
  const trueWords = pickN(truePool.filter(w => !usedWords.has(w)), trueCount, rng);
  if (trueWords.length < trueCount) return null;
  if (distractorWords.length + backfillWords.length < falseCount) return null;

  trueWords.forEach(w   => usedWords.add(w));
  backfillWords.forEach(w => usedWords.add(w));

  const falseWords = [...distractorWords, ...backfillWords];

  return shuffle([
    ...trueWords.map(w  => ({ word: w, shouldAccept: true })),
    ...falseWords.map(w => ({ word: w, shouldAccept: false })),
  ], rng);
}

function buildPairPhase(rule, count, rng, usedWords) {
  const half       = Math.floor(count / 2);
  const trueCount  = half;
  const falseCount = count - half;

  const pairCatMap = { 'P09':'animal', 'P10':'color' };
  const pairCat    = pairCatMap[rule.id];

  // P11 (ends with a vowel) and P12 (same number of vowels) must exclude Y-containing words
  const PAIR_VOWEL_RULE_IDS = new Set(['P11','P12']);
  const basePool = pairCat
    ? CATEGORY_POOL[pairCat]
    : getSingleWordPool();
  const allWords = PAIR_VOWEL_RULE_IDS.has(rule.id)
    ? basePool.filter(w => !w.includes('Y'))
    : basePool;

  let truePairs  = [];
  let falsePairs = [];
  let attempts   = 0;

  while (truePairs.length < trueCount && attempts < 5000) {
    attempts++;
    const l = pick(allWords, rng);
    const r = pick(allWords, rng);
    if (l===r) continue;
    if (usedWords.has(l)||usedWords.has(r)) continue;
    if (usedWords.has(l+'+'+r)||usedWords.has(r+'+'+l)) continue;
    if (!rule.check(l,r)) continue;
    truePairs.push({ left:l, right:r, shouldAccept:true });
    usedWords.add(l); usedWords.add(r); usedWords.add(l+'+'+r);
  }
  attempts = 0;
  while (falsePairs.length < falseCount && attempts < 5000) {
    attempts++;
    const l = pick(allWords, rng);
    const r = pick(allWords, rng);
    if (l===r) continue;
    if (usedWords.has(l)||usedWords.has(r)) continue;
    if (usedWords.has(l+'+'+r)||usedWords.has(r+'+'+l)) continue;
    if (rule.check(l,r)) continue;
    falsePairs.push({ left:l, right:r, shouldAccept:false });
    usedWords.add(l); usedWords.add(r); usedWords.add(l+'+'+r);
  }

  if (truePairs.length < trueCount || falsePairs.length < falseCount) return null;
  return shuffle([...truePairs, ...falsePairs], rng);
}

function buildMixedPhase(rule, count, rng, usedWords) {
  const pairCount   = Math.floor(count / 2);
  const singleCount = count - pairCount;
  const half        = Math.floor(singleCount / 2);

  const pool     = getPoolForRule(rule);
  const truePool = pool.filter(w => rule.check(w) && !usedWords.has(w));

  const broadSingle = noYFilter(rule, getSingleWordPool())
    .filter(w => !rule.check(w) && !usedWords.has(w));

  const falsePool = CAT_RULE_IDS.has(rule.id)
    ? [...new Set([
        ...pool.filter(w => !rule.check(w) && !usedWords.has(w)),
        ...broadSingle,
      ])]
    : broadSingle;

  // ── Distractor logic: 50% of single false slots from distractors ──
  const singleFalseCount  = singleCount - half;
  const distractorSlots   = Math.floor(singleFalseCount * 0.5);
  const rawDistractors    = (DISTRACTOR_POOL[rule.id] || [])
    .filter(w => !rule.check(w) && !usedWords.has(w));
  const distractorWords   = pickN(rawDistractors, distractorSlots, rng);
  distractorWords.forEach(w => usedWords.add(w));

  const backfillPool  = falsePool.filter(w => !usedWords.has(w));
  const backfillCount = singleFalseCount - distractorWords.length;
  const backfillWords = pickN(backfillPool, backfillCount, rng);

  const trueWords  = pickN(truePool.filter(w => !usedWords.has(w)), half, rng);
  const falseWords = [...distractorWords, ...backfillWords];

  if (trueWords.length < half || falseWords.length < singleFalseCount) return null;

  trueWords.forEach(w   => usedWords.add(w));
  backfillWords.forEach(w => usedWords.add(w));

  const singleItems = shuffle([
    ...trueWords.map(w  => ({ format:'single', word:w, shouldAccept:true })),
    ...falseWords.map(w => ({ format:'single', word:w, shouldAccept:false })),
  ], rng);

  // R3 pairs use single-word rules — both words must satisfy rule
  // For category rules, sample true pairs directly from the category pool (avoids needle-in-haystack)
  // For false pairs, sample from the broad single-word pool
  const fullSinglePool = noYFilter(rule, getSingleWordPool()).filter(w => !isMultiWord(w));
  // True pair pool: words that satisfy the rule and are single-word
  const truePairPool  = fullSinglePool.filter(w => rule.check(w) && !usedWords.has(w));
  // False pair pool: words from broad pool that do NOT both satisfy — sample the broad pool
  const falsePairPool = fullSinglePool;

  const halfP     = Math.floor(pairCount / 2);
  let truePairs   = [];
  let falsePairs  = [];
  let att         = 0;

  // Build true pairs from truePairPool (direct sampling — efficient for category rules)
  const shuffledTruePairPool = shuffle([...truePairPool], rng);
  for (let i = 0; i < shuffledTruePairPool.length - 1 && truePairs.length < halfP; i++) {
    const l = shuffledTruePairPool[i];
    if (usedWords.has(l)) continue;
    for (let j = i + 1; j < shuffledTruePairPool.length && truePairs.length < halfP; j++) {
      const r = shuffledTruePairPool[j];
      if (usedWords.has(r)) continue;
      if (usedWords.has(l+'+'+r)) continue;
      if (!rule.check(l) || !rule.check(r)) continue;
      truePairs.push({ format:'pair', left:l, right:r, shouldAccept:true });
      usedWords.add(l); usedWords.add(r); usedWords.add(l+'+'+r);
      break;
    }
  }

  // Build false pairs via random sampling (at least one word must fail rule)
  att = 0;
  while (falsePairs.length < pairCount - halfP && att < 3000) {
    att++;
    const l = pick(falsePairPool, rng);
    const r = pick(falsePairPool, rng);
    if (l===r) continue;
    if (usedWords.has(l)||usedWords.has(r)||usedWords.has(l+'+'+r)) continue;
    if (rule.check(l)&&rule.check(r)) continue;
    falsePairs.push({ format:'pair', left:l, right:r, shouldAccept:false });
    usedWords.add(l); usedWords.add(r); usedWords.add(l+'+'+r);
  }

  if (truePairs.length < halfP || falsePairs.length < pairCount - halfP) return null;
  const pairItems = shuffle([...truePairs, ...falsePairs], rng);
  return shuffle([...singleItems, ...pairItems], rng);
}


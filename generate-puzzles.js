// Rule picker — enforces family grouping
// ══════════════════════════════════════════════════════════════════
function pickDistinctRules(rng, recentRules, dayNumber) {
  const recent         = recentRules || new Set();
  const structFamilies = ['PATTERN','SHAPE','VOWEL','LANGUAGE'];
  const knowFamilies   = ['WORLD','CULTURE'];
  const pairFamilies   = ['PATTERN','SHAPE','VOWEL','WORLD'];

  // Alternate supergroups by day so every puzzle has structural + knowledge rounds
  const r1Super = (dayNumber % 2 === 1) ? 'STRUCTURAL' : 'KNOWLEDGE';
  const r3Super = r1Super === 'STRUCTURAL' ? 'KNOWLEDGE' : 'STRUCTURAL';

  const r1FamilyPool = r1Super === 'STRUCTURAL' ? structFamilies : knowFamilies;
  const r3FamilyPool = r3Super === 'STRUCTURAL' ? structFamilies : knowFamilies;

  const availSingle = SINGLE_RULES.filter(r => !recent.has(r.label));
  const availPair   = PAIR_RULES.filter(r => !recent.has(r.label));
  const singlePool  = availSingle.length >= 10 ? availSingle : SINGLE_RULES;
  const pairPool    = availPair.length >= 3    ? availPair   : PAIR_RULES;

  function pickFamilyRules(superFamilies, needed, pool, exclude) {
    const ex = exclude || [];
    const filteredPool = pool.filter(r => !ex.find(x => x.label === r.label));
    const shuffledFamilies = shuffle([...superFamilies], rng);
    for (const primaryFam of shuffledFamilies) {
      const adjacent = ADJACENT_FAMILIES[primaryFam];
      const eligible = filteredPool.filter(r => adjacent.has(r.family));
      const shuffled = shuffle(eligible, rng);
      if (shuffled.length >= needed) return shuffled.slice(0, needed);
    }
    // Fallback: any rule from supergroup
    const fallback = shuffle(
      filteredPool.filter(r => superFamilies.some(f => ADJACENT_FAMILIES[f].has(r.family))),
      rng
    );
    if (fallback.length >= needed) return fallback.slice(0, needed);
    // Last resort: ignore exclude
    const last = shuffle(pool.filter(r => superFamilies.some(f => ADJACENT_FAMILIES[f].has(r.family))), rng);
    return last.slice(0, needed);
  }

  const r3Pool  = singlePool.filter(r => !r.r1Only);
  const r1Rules = pickFamilyRules(r1FamilyPool, 2, singlePool);
  const r3Rules = pickFamilyRules(r3FamilyPool, 4, r3Pool, r1Rules);
  const r2Rules = pickFamilyRules(pairFamilies, 3, pairPool);

  return { r1Rules, r2Rules, r3Rules };
}


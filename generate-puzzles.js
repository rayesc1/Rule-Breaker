// Puzzle builder
// ══════════════════════════════════════════════════════════════════
const LAUNCH_UTC = Date.UTC(2026, 2, 13);

function utcDateString(dayNumber) {
  const ms = LAUNCH_UTC + (dayNumber - 1) * 86400000;
  const d  = new Date(ms);
  return d.toLocaleDateString('en-US', { timeZone:'UTC', year:'numeric', month:'long', day:'numeric' });
}

function buildPuzzle(dayNumber, recentWords, recentRules) {
  const usedBase = new Set(recentWords || []);
  let attempts   = 0;

  while (attempts < 50) {
    attempts++;
    const rngLocal  = makePrng(dayNumber * 31337 + 99991 + attempts * 7);
    const usedLocal = new Set(usedBase);
    const { r1Rules, r2Rules, r3Rules } = pickDistinctRules(rngLocal, recentRules, dayNumber);

    const r1p0 = buildSinglePhase(r1Rules[0], 15, rngLocal, usedLocal); if (!r1p0) continue;
    const r1p1 = buildSinglePhase(r1Rules[1], 15, rngLocal, usedLocal); if (!r1p1) continue;

    const r2p0 = buildPairPhase(r2Rules[0], 10, rngLocal, usedLocal); if (!r2p0) continue;
    const r2p1 = buildPairPhase(r2Rules[1], 10, rngLocal, usedLocal); if (!r2p1) continue;
    const r2p2 = buildPairPhase(r2Rules[2], 10, rngLocal, usedLocal); if (!r2p2) continue;

    const r3p0 = buildMixedPhase(r3Rules[0], 10, rngLocal, usedLocal); if (!r3p0) continue;
    const r3p1 = buildMixedPhase(r3Rules[1], 10, rngLocal, usedLocal); if (!r3p1) continue;
    const r3p2 = buildMixedPhase(r3Rules[2], 10, rngLocal, usedLocal); if (!r3p2) continue;
    const r3p3 = buildMixedPhase(r3Rules[3], 10, rngLocal, usedLocal); if (!r3p3) continue;

    return {
      day:    dayNumber,
      number: String(dayNumber).padStart(3, '0'),
      date:   utcDateString(dayNumber),
      rounds: [
        {
          id:0, label:'ROUND 1', name:'The Switch',
          type:'single', total:30, inversionPoints:[15],
          rules: [r1Rules[0].label, r1Rules[1].label],
          words: [...r1p0, ...r1p1],
        },
        {
          id:1, label:'ROUND 2', name:'Pairs',
          type:'pairs', total:30, inversionPoints:[10,20],
          rules: [r2Rules[0].label, r2Rules[1].label, r2Rules[2].label],
          pairs: [...r2p0, ...r2p1, ...r2p2],
        },
        {
          id:2, label:'ROUND 3', name:'Rule Breaker',
          type:'mixed', total:40, inversionPoints:[10,20,30],
          rules: [r3Rules[0].label, r3Rules[1].label, r3Rules[2].label, r3Rules[3].label],
          items: [...r3p0, ...r3p1, ...r3p2, ...r3p3],
        },
      ],
    };
  }
  throw new Error(`Failed to build puzzle for day ${dayNumber} after ${attempts} attempts`);
}

// ══════════════════════════════════════════════════════════════════
// Validation
// ══════════════════════════════════════════════════════════════════
function validatePuzzle(puzzle) {
  const errors = [];
  const singleRuleMap = Object.fromEntries(SINGLE_RULES.map(r => [r.label, r]));
  const pairRuleMap   = Object.fromEntries(PAIR_RULES.map(r => [r.label, r]));

  puzzle.rounds.forEach(rd => {
    let phase = 0;
    const items = rd.type==='single' ? rd.words
                : rd.type==='pairs'  ? rd.pairs
                : rd.items;

    // Check total count
    if (items.length !== rd.total) {
      errors.push(`R${rd.id}: expected ${rd.total} items, got ${items.length}`);
    }

    items.forEach((item, i) => {
      if (rd.inversionPoints.includes(i)) phase++;
      const ruleLabel = rd.rules[phase];

      if (rd.type==='single' || (rd.type==='mixed' && item.format==='single')) {
        const rule = singleRuleMap[ruleLabel];
        if (!rule) { errors.push(`R${rd.id} i${i}: no rule for "${ruleLabel}"`); return; }

        // Guard: multi-word items must only appear under category rules
        if (isMultiWord(item.word) && !CAT_RULE_IDS.has(rule.id)) {
          errors.push(`R${rd.id} i${i}: multi-word "${item.word}" used under computational rule "${ruleLabel}"`);
          return;
        }

        const computed = rule.check(item.word);
        if (computed !== item.shouldAccept) {
          errors.push(`R${rd.id} i${i} "${item.word}" [${ruleLabel}]: computed=${computed} expected=${item.shouldAccept}`);
        }
      } else {
        // Pair item
        const isPairRule = !!pairRuleMap[ruleLabel];
        if (isPairRule) {
          const rule    = pairRuleMap[ruleLabel];
          const computed = rule.check(item.left, item.right);
          if (!!computed !== item.shouldAccept) {
            errors.push(`R${rd.id} i${i} "${item.left}/${item.right}" [${ruleLabel}]: computed=${computed} expected=${item.shouldAccept}`);
          }
        } else {
          // R3 mixed pairs use single-word rules — both must satisfy
          const rule = singleRuleMap[ruleLabel];
          if (!rule) { errors.push(`R${rd.id} i${i}: no rule for "${ruleLabel}"`); return; }
          const computed = rule.check(item.left) && rule.check(item.right);
          if (!!computed !== item.shouldAccept) {
            errors.push(`R${rd.id} i${i} "${item.left}/${item.right}" [${ruleLabel}]: computed=${computed} expected=${item.shouldAccept}`);
          }
        }
      }
    });

    // Check no consecutive-day supergroup clash (warn, not error)
    // This is enforced at pick time, so just verify family consistency
    const roundRules = rd.rules.map(l => singleRuleMap[l] || pairRuleMap[l]).filter(Boolean);
    const supergroups = new Set(roundRules.map(r => r.supergroup));
    if (supergroups.size > 1 && rd.type !== 'pairs') {
      errors.push(`R${rd.id}: mixed supergroups in round — ${[...supergroups].join(', ')}`);
    }
  });

  return errors;
}

